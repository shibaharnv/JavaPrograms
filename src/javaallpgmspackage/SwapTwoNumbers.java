package javaallpgmspackage;

public class SwapTwoNumbers {

    public static void main(String[] args) {

        int a=10;

        int b=20;
        //Swap two numbers:

        int temp;

        temp=a;

        a=b;
        b=temp;

        System.out.println("The value of a is "+a);
        System.out.println("The value of b is "+b);

    }
}
